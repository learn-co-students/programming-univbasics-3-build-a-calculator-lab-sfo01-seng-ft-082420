def addition(num1, num2)
  add = num1 + num2
  return add
end

def subtraction(num1, num2)
  subt = num1 - num2 
  return subt
end

def division(num1, num2)
  divi = num1 / num2
  return divi
end

def multiplication(num1, num2)
  mulit = num1 * num2
  return mulit
end

def modulo(num1, num2)
  mod = num1 % num2
  return mod
end

def square_root(num)
  root = Math.sqrt(num)
  return root 
end